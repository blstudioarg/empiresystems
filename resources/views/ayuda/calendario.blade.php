<p>El calendario muestra de un vistazo el <strong>cumplimiento de fichaje</strong> del equipo: cada
día se pinta según cómo se cumplió el horario (a tiempo, con retraso, ausencia, etc.). Los números
de arriba resumen el período que estás viendo.</p>

<ol>
	<li><strong>Leé el color</strong>: cada día lleva una etiqueta con su veredicto. La leyenda te
		dice qué significa cada uno.</li>
	<li><strong>Cambiá el período</strong>: navegá entre meses/semanas con los controles del
		calendario para ver otras fechas.</li>
	<li><strong>Profundizá</strong>: para el detalle de un empleado o corregir fichajes, pasá a
		<strong>Jornada</strong> o <strong>Miembros</strong>.</li>
</ol>

<p class="ayuda-nota">El calendario es una lectura de lo ya registrado; no se ficha ni se corrige
desde acá.</p>
