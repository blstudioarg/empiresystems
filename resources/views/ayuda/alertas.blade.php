<p>Las <strong>alertas</strong> te avisan de incidencias de fichaje del equipo: retrasos,
ausencias, jornadas sin cerrar y similares. El sistema las genera solo, comparando lo fichado
contra el horario asignado.</p>

<ol>
	<li><strong>Nuevas</strong>: las que todavía no revisaste. El sidebar te muestra un contador
		para que no se te pasen.</li>
	<li><strong>Resolver</strong>: cuando ya revisaste o corregiste una incidencia, la marcás como
		resuelta para sacarla de las pendientes.</li>
</ol>

<p class="ayuda-nota">Una alerta señala algo a revisar, no corrige nada por sí sola: la corrección
del fichaje se hace en <strong>Jornada</strong>.</p>
