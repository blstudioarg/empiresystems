<p>Este es el <strong>detalle de una campaña</strong> ya enviada: cómo le fue. Ves cuántos correos
salieron bien y cuántos fallaron.</p>

<ol>
	<li><strong>Resultado</strong>: total de destinatarios, enviados con éxito y fallidos.</li>
	<li><strong>Reintentar</strong>: si algunos correos fallaron (dirección caída, error temporal),
		podés reintentar el envío solo a esos, sin remandar a los que ya recibieron.</li>
</ol>

<p class="ayuda-nota">Un fallido suele ser una dirección inválida o un rechazo del servidor del
destinatario; reintentar tiene sentido para caídas temporales, no para emails mal escritos.</p>
