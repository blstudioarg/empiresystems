<p>Las <strong>plantillas de email</strong> son moldes reutilizables de correo: escribís el diseño
y el texto una vez, y después los usás en tus campañas sin rehacerlos cada vez.</p>

<ol>
	<li><strong>Nueva plantilla</strong>: le ponés nombre, asunto y cuerpo del correo.</li>
	<li><strong>Activas / inactivas</strong>: solo las activas aparecen disponibles al crear una
		campaña; desactivá las que no quieras seguir usando sin borrarlas.</li>
</ol>

<p class="ayuda-nota">Crear una plantilla no envía nada: el envío se hace desde
<strong>Campañas</strong>, eligiendo esta plantilla como base.</p>
