<p>Acá armás una <strong>campaña de email</strong>: el correo que vas a enviar y a quién le llega.
Se compone en dos partes —el mensaje y los destinatarios— y se envía al confirmar.</p>

<ol>
	<li><strong>Composición</strong>: escribí el asunto y el cuerpo. Podés partir de una
		<strong>plantilla</strong> para no empezar de cero (opcional).</li>
	<li><strong>Destinatarios</strong>: elegís a quiénes se manda.</li>
	<li><strong>Enviar</strong>: al confirmar, el envío se procesa y una barra te muestra el
		progreso en vivo (cuántos van saliendo).</li>
</ol>

<p class="ayuda-nota">El envío no se puede deshacer una vez arranca: revisá bien asunto, contenido
y destinatarios antes de confirmar.</p>
