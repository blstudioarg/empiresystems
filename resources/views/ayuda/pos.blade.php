<p>Acá ves los <strong>tickets</strong> ya emitidos: las facturas simplificadas del punto de venta
(TPV), pensadas para ventas rápidas de mostrador sin cargar todos los datos del cliente.</p>

<ol>
	<li><strong>Nuevo ticket</strong>: abre la pantalla de venta rápida (grid de productos) para
		cobrar en el momento.</li>
	<li><strong>Ver / imprimir</strong>: desde Acciones abrís la vista previa del ticket para
		reimprimirlo o descargarlo.</li>
</ol>

<p class="ayuda-nota">Un ticket es una factura simplificada: se emite en el acto y, como toda
factura emitida, no se edita después. Para importes altos o clientes que necesitan factura
completa con sus datos, usá <strong>Facturas</strong>.</p>
