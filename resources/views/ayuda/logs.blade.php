<p>El <strong>historial de actividad</strong> registra quién hizo qué en el sistema: accesos,
acciones importantes y su resultado. Sirve para auditar y para entender qué pasó ante una duda.</p>

<ol>
	<li><strong>Qué se registra</strong>: fecha, usuario, acción, si salió bien o falló, más datos
		técnicos (IP, navegador, ubicación aproximada).</li>
	<li><strong>Buscar</strong>: usá el buscador para filtrar por usuario o tipo de acción cuando
		investigás algo puntual.</li>
</ol>

<p class="ayuda-nota">Es un registro de solo lectura: no se edita ni se borra, justamente para que
sirva como evidencia fiable.</p>
