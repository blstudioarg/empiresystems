<p>Un <strong>rol</strong> es un conjunto de <strong>permisos</strong>: define qué secciones del
sistema puede ver y usar quien lo tenga. Acá creás y ajustás los roles de tu empresa.</p>

<ol>
	<li><strong>Crear un rol</strong>: le ponés nombre (ej. “Vendedor”, “Contable”) y marcás qué
		permisos incluye del catálogo.</li>
	<li><strong>Permisos</strong>: cada permiso habilita una sección (ver clientes, ver facturas,
		etc.). Lo que no marcás, ese rol no lo ve en el menú ni puede acceder.</li>
	<li><strong>Asignar</strong>: después, en <strong>Usuarios</strong>, le das el rol a cada
		persona.</li>
</ol>

<p class="ayuda-nota">El rol <strong>Administrador</strong> siempre tiene todos los permisos y
recibe automáticamente los nuevos; el resto de roles solo tienen lo que les marcaste a propósito.</p>
