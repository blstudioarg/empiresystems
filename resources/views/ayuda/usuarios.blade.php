<p>Acá gestionás las <strong>personas que acceden</strong> a tu empresa en el sistema: quién entra,
con qué rol y en qué estado. Un usuario es una cuenta de acceso; el <strong>rol</strong> define qué
puede ver y hacer.</p>

<ol>
	<li><strong>Pendientes de aprobación</strong>: cuando alguien se registra, queda en espera
		hasta que lo aprobás. Hasta entonces no puede entrar.</li>
	<li><strong>Asignar rol</strong>: a cada usuario le das un rol (Administrador, Usuario, o los
		que hayas creado). El rol es lo que le habilita las secciones del menú.</li>
	<li><strong>Activar / desactivar</strong>: podés cortar el acceso de alguien sin borrar su
		cuenta ni su historial.</li>
</ol>

<p class="ayuda-nota">Qué permisos tiene cada rol se configura en <strong>Roles</strong>, no acá:
esta pantalla asigna el rol a la persona, la otra define qué puede hacer ese rol.</p>
