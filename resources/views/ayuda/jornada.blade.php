<p>Este es el <strong>registro de jornada del equipo</strong>: la herramienta de control para ver
qué fichó cada empleado, comparar contra su horario y sacar el informe legal de jornada.</p>

<ol>
	<li><strong>Consultar</strong>: elegí un empleado y un rango de fechas para ver todos sus
		fichajes y sus horas trabajadas.</li>
	<li><strong>Corregir un fichaje</strong>: si alguien olvidó fichar o marcó mal, podés corregir
		la hora — queda registrado que fue una corrección manual.</li>
	<li><strong>Informe</strong>: generás el informe de jornada (obligatorio por ley de registro
		horario) para descargar o imprimir.</li>
</ol>

<p class="ayuda-nota">Cada empleado ve solo lo suyo en “Mi jornada”; esta pantalla es para quien
gestiona el equipo y ve a todos.</p>
