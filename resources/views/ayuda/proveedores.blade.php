<p>Acá gestionás tus proveedores: a quién le comprás. Estos datos se usan al registrar
<strong>compras</strong> y al importar facturas de proveedor. Se crean y editan con
<strong>“Agregar proveedor”</strong> y el menú <strong>Acciones</strong> de cada fila.</p>

<ol>
	<li><strong>Nombre / razón social</strong> y <strong>NIF</strong>: el NIF es la clave que
		identifica al proveedor — al importar un Facturae, la compra se asocia por NIF.</li>
	<li><strong>Contacto y dirección</strong>: email, teléfono, provincia y ciudad. Elegí primero
		la provincia y la ciudad se filtra sola.</li>
</ol>

<p class="ayuda-nota">Un proveedor no se puede eliminar si ya tiene compras registradas; en ese
caso solo podés editarlo.</p>
