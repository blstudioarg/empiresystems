<p>Acá configurás tu empresa. Está dividido en pestañas; cada una agrupa un tipo de ajuste:</p>

<ol>
	<li><strong>General</strong>: datos fiscales de tu empresa (nombre, NIF, dirección) que salen
		en las facturas.</li>
	<li><strong>Apariencia / Marca</strong>: logo, favicon y los colores de tu marca en el panel.</li>
	<li><strong>Facturación</strong>: series y numeración, cuentas bancarias y demás ajustes que
		afectan a cómo se emiten las facturas.</li>
	<li><strong>Email, Archivos, Certificado, Fichajes</strong>: envío de correos, límites de
		almacenamiento, tu certificado digital para firmar y las reglas de fichaje.</li>
</ol>

<p class="ayuda-nota">Los datos de General y el Certificado son los que usa la facturación legal:
revisalos bien antes de empezar a emitir.</p>
