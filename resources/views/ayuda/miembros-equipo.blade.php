<p>Acá definís tu <strong>equipo</strong>: qué usuarios son empleados que fichan, con su puesto, su
ubicación de trabajo y su horario. Esta configuración es la que después usan Fichar, Jornada y el
Calendario.</p>

<ol>
	<li><strong>Alta de miembro</strong>: elegís el usuario y su puesto.</li>
	<li><strong>Ubicación de trabajo</strong>: fijás la dirección en el mapa y una
		<strong>distancia máxima (m)</strong>. El empleado solo podrá fichar si está dentro de ese
		radio — así se valida por GPS que ficha desde el lugar correcto.</li>
	<li><strong>Horario</strong>: le asignás un horario con fecha “vigente desde”. Podés cambiarlo
		en el tiempo; el historial de horarios queda guardado.</li>
</ol>

<p class="ayuda-nota">Los horarios se crean primero en la pantalla <strong>Horarios</strong>; acá
solo se asignan al miembro. Guardá el miembro antes de poder asignarle uno.</p>
