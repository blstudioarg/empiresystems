<p>Esta es tu pantalla para <strong>fichar</strong>: registrar cuándo entrás, cuándo hacés una
pausa y cuándo salís. El reloj de arriba muestra la hora y en qué estado estás ahora.</p>

<ol>
	<li><strong>Entrada</strong>: al empezar tu jornada. Queda registrada la hora exacta.</li>
	<li><strong>Pausa / Reanudar</strong>: para cortes (comida, descanso). El tiempo en pausa no
		cuenta como trabajado.</li>
	<li><strong>Salida</strong>: al terminar. Cierra la jornada del día.</li>
</ol>

<p class="ayuda-nota">Los botones se activan según tu estado: si ya estás dentro no podés “entrar”
otra vez. Si tu puesto tiene ubicación asignada, puede pedirte permiso de ubicación para validar
que fichás desde el lugar de trabajo. Si tocás <strong>Fichar salida</strong> por error, tenés
unos segundos para tocar “Deshacer” en el aviso que aparece y tu jornada sigue abierta.</p>
