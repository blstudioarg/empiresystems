<p>Acá modificás una compra que todavía está en <strong>borrador</strong>: corregís proveedor,
documento, fecha, líneas o notas antes de confirmarla.</p>

<ol>
	<li><strong>Ajustá lo que haga falta</strong>: los mismos datos que al crearla.</li>
	<li><strong>Guardar cambios</strong>: se actualiza el borrador.</li>
</ol>

<p class="ayuda-nota">Solo se editan las compras en borrador. Una vez confirmada (ya movió stock),
no se edita: hay que anularla para revertir el stock y volver a cargarla.</p>
