<p>Este es el editor de factura. La pantalla se ve como el documento que vas a emitir: a medida
que cargás datos, el papel de la derecha se actualiza igual que saldrá en el PDF.</p>

<ol>
	<li><strong>Cliente y fechas</strong>: elegí el cliente en el membrete y ajustá la fecha de la
		factura.</li>
	<li><strong>Agregar líneas</strong>: desde el catálogo de la izquierda tocás un artículo y se
		suma como línea. Usá “+ Línea libre” para un concepto suelto sin artículo del catálogo.</li>
	<li><strong>Cantidades e importes</strong>: editá cantidad, precio y descuento en cada línea;
		la base, el IVA y el total se recalculan solos abajo.</li>
	<li><strong>Guardar</strong>: se guarda como <strong>borrador</strong>. Podés seguir editándola;
		la emisión con número fiscal se hace después, desde el listado de facturas.</li>
</ol>

<p class="ayuda-nota">Los importes que valen son los que calcula el servidor al guardar; el total
que ves mientras editás es una vista previa orientativa.</p>
