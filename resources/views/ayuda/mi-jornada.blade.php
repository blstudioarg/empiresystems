		pausas previstas).</li>
	<li><strong>Consultar un período</strong>: elegí las fechas y tocá “Consultar” para ver tus
		fichajes y el total de horas trabajadas en ese rango.</li>
</ol>

<p class="ayuda-nota">Para registrar entradas y salidas usá la pantalla <strong>Fichar</strong>;
esta es solo de consulta.</p>
