<p>Acá cargás una <strong>compra nueva</strong> a mano: la factura que te pasó un proveedor. Se
guarda como <strong>borrador</strong> para que puedas revisarla antes de confirmarla.</p>

<ol>
	<li><strong>Proveedor y documento</strong>: elegí el proveedor, poné el nº de documento que
		figura en su factura y la fecha.</li>
	<li><strong>Líneas</strong>: agregá los artículos comprados con su cantidad y precio.</li>
	<li><strong>Guardar borrador</strong>: queda registrada pero todavía no toca el stock.</li>
</ol>

<p class="ayuda-nota">Guardar no repone stock: eso ocurre recién al <strong>confirmar</strong> la
compra desde su detalle. Si el proveedor te mandó un XML Facturae, mejor importalo desde el listado
en vez de cargarla a mano.</p>
