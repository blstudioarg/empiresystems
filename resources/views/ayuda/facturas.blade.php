<p>Acá están todas tus facturas de venta. Cada una pasa por estados: nace como
<strong>borrador</strong> (editable) y, al emitirla, se vuelve <strong>emitida</strong> con número
fiscal definitivo. Filtrá por tipo con los botones de arriba.</p>

<ol>
	<li><strong>Nueva factura</strong>: te lleva al editor de factura para cargar cliente, líneas
		e importes.</li>
	<li><strong>Emitir</strong> (menú Acciones): asigna el número fiscal y registra la factura en
		Verifactu. <strong>Desde ese momento no se puede editar ni borrar.</strong></li>
	<li><strong>Cobros</strong>: registrá los pagos que vas recibiendo; la columna “Cobro” muestra
		si está pendiente, parcial o cobrada.</li>
	<li><strong>Rectificar</strong>: si una factura emitida tiene un error, no se borra — se crea
		una <strong>rectificativa</strong> que la corrige (lo que exige la normativa).</li>
</ol>

<p class="ayuda-nota">Una factura emitida es un documento legal: no se edita ni se elimina. Todo
cambio posterior va por una rectificativa.</p>
