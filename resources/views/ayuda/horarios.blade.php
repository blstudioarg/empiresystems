<p>Acá definís los <strong>horarios de trabajo</strong> de tu empresa: las plantillas de jornada
(hora de entrada, salida y pausas) que después asignás a cada miembro del equipo.</p>

<ol>
	<li><strong>Nuevo horario</strong>: le ponés nombre y definís los tramos de cada día de la
		semana.</li>
	<li><strong>Reutilizá</strong>: un mismo horario sirve para varios empleados; no hace falta
		crear uno por persona si comparten jornada.</li>
</ol>

<p class="ayuda-nota">Crear el horario acá no lo asigna a nadie: la asignación a cada empleado se
hace desde <strong>Miembros</strong>. El horario es lo que permite detectar retrasos y ausencias.</p>
