<p>Acá gestionás el <strong>pipeline comercial</strong>: oportunidades de venta vinculadas a un
lead o a un cliente, organizadas por etapa.</p>

<ol>
	<li><strong>Crear</strong>: elegí un lead o un cliente (exactamente uno) y un importe
		estimado opcional. El pipeline agrupa automáticamente por etapa.</li>
	<li><strong>Etapas</strong>: <code>nueva → en negociación → ganada/perdida</code>. Ganada y
		perdida son finales — no se puede volver atrás.</li>
	<li><strong>Ganar</strong>: si la oportunidad viene de un lead, al ganarla ese lead se
		convierte automáticamente en cliente.</li>
	<li><strong>Perder</strong>: pide un motivo obligatorio y bloquea la creación de nuevos
		presupuestos para esa oportunidad.</li>
</ol>
