<p>Acá gestionás tu cartera de clientes: los datos que después se usan para emitir facturas
(nombre o razón social, NIF, dirección fiscal). Se crean y editan desde el botón
<strong>“Agregar cliente”</strong> y el menú <strong>Acciones</strong> de cada fila.</p>

<ol>
	<li><strong>Tipo</strong>: “Particular” o “Empresa” cambia qué dato es el principal
		(Nombre vs. Razón social) y cómo figura el cliente en la factura.</li>
	<li><strong>NIF / CIF / NIE</strong>: necesario para poder facturarle — Verifactu lo exige.</li>
	<li><strong>Provincia y ciudad</strong>: elegí primero la provincia; la lista de ciudades se
		filtra sola según esa elección.</li>
	<li><strong>Recargo de equivalencia</strong>: marcalo solo si el cliente es un minorista
		sujeto a ese régimen especial de IVA. Afecta cómo se calculan sus facturas.</li>
</ol>

<p class="ayuda-nota">Un cliente no se puede eliminar si ya tiene facturas asociadas; en ese
	caso solo podés editarlo.</p>
