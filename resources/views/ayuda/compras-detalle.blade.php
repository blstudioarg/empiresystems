<p>Este es el <strong>detalle de una compra</strong>: sus datos, líneas e importes, y las acciones
según su estado.</p>

<ol>
	<li><strong>Confirmar (repone stock)</strong>: pasa la compra de borrador a confirmada y
		<strong>suma al inventario</strong> las unidades de cada producto con gestión de stock.</li>
	<li><strong>Anular (revierte stock)</strong>: deshace una compra confirmada y
		<strong>descuenta</strong> del stock lo que había sumado.</li>
	<li><strong>Editar</strong>: solo disponible mientras esté en borrador.</li>
	<li><strong>Estado B2B</strong>: si la compra vino por vía electrónica, actualizá su ciclo
		(aceptada, pagada, etc.) desde el selector.</li>
</ol>

<p class="ayuda-nota">Confirmar y anular son las únicas acciones que tocan el stock; hacelas con
cuidado porque quedan reflejadas en el Kardex.</p>
