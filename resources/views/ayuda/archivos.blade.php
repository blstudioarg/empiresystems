<p>Este es tu <strong>gestor de archivos</strong>: un espacio para guardar y organizar documentos
de la empresa (contratos, PDFs, imágenes) en carpetas, como un disco en la nube.</p>

<ol>
	<li><strong>Subir archivo</strong>: lo cargás a la carpeta en la que estés parado.</li>
	<li><strong>Carpetas</strong>: creá carpetas para organizar; entrás y salís navegando como en
		tu computadora.</li>
	<li><strong>Renombrar y mover</strong>: desde las acciones de cada archivo o carpeta podés
		renombrarlos o moverlos a otra carpeta.</li>
</ol>

<p class="ayuda-nota">El espacio disponible es limitado por tu plan: mirá el indicador de “Espacio
usado” arriba para no quedarte sin lugar.</p>
