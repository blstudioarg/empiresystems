<p>El <strong>Kardex</strong> es el historial de todos los movimientos de inventario: cada entrada
y salida de stock, con su origen y el stock que quedó después. Es la única foto fiel de cómo se
movió cada producto.</p>

<ol>
	<li><strong>Movimientos automáticos</strong>: las compras confirmadas suman stock (entradas) y
		las ventas lo restan (salidas). No hay que cargarlos a mano.</li>
	<li><strong>Ajuste manual</strong>: para corregir diferencias (roturas, mermas, recuento
		físico) usá <strong>“+ Ajuste manual”</strong> e indicá la cantidad y el motivo.</li>
	<li><strong>Alertas de stock mínimo</strong>: los artículos que bajan de su mínimo aparecen
		destacados arriba para que sepas qué reponer.</li>
</ol>

<p class="ayuda-nota">Los productos se dan de alta y se marcan “con gestión de stock” en el
<strong>Catálogo</strong>, no acá. Este panel solo registra y ajusta movimientos.</p>
