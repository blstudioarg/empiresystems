<p>Las <strong>campañas de email</strong> te permiten enviar un correo a muchos contactos a la vez
(novedades, promociones, avisos). Acá ves las enviadas y su resultado, y creás nuevas.</p>

<ol>
	<li><strong>Nueva campaña</strong>: elegís a quién le llega, el asunto y el contenido (podés
		partir de una plantilla).</li>
	<li><strong>Enviados / fallidos</strong>: tras el envío, cada campaña muestra cuántos correos
		llegaron y cuántos fallaron.</li>
</ol>

<p class="ayuda-nota">El diseño reutilizable de los correos vive en <strong>Plantillas de
email</strong>; una campaña es un envío puntual, la plantilla es el molde.</p>
