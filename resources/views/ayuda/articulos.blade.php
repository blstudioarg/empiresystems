<p>Este es tu catálogo: los <strong>productos y servicios</strong> que después cargás en facturas
y tickets. Se crean y editan con el botón <strong>“Agregar artículo”</strong> y el menú
<strong>Acciones</strong> de cada fila.</p>

<ol>
	<li><strong>Tipo</strong>: “Producto” (algo físico, puede llevar control de stock) o “Servicio”
		(no tiene stock). La opción de stock solo aparece en productos.</li>
	<li><strong>Precio y tipo impositivo</strong>: el precio es sin impuestos; el tipo impositivo
		(IVA/IGIC) se aplica al facturar. Solo salen los tipos válidos según tu régimen.</li>
	<li><strong>Categoría y unidad</strong>: son catálogos propios — podés crear entradas nuevas al
		vuelo con el botón <strong>+</strong> que hay al lado de cada selector.</li>
	<li><strong>Gestionar stock</strong> (solo productos): al activarlo cargás el stock actual y un
		stock mínimo. El sistema avisa cuando un producto baja de ese mínimo.</li>
</ol>

<p class="ayuda-nota">El stock no se edita a mano desde acá una vez creado el artículo: se mueve
solo con las compras, las ventas y los ajustes del <strong>Kardex</strong>.</p>
